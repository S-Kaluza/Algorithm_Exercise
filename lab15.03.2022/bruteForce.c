int bruteForce(int N, int* tab)
{
    int sum = 0;
    for(int i = 0; j < N-3; i++){
        for(int j = 0; j < N; j++){
            for(int k = 0; k < N; k++){
                for(int l = 0; l < N; l++){
                    if(tab[i] + tab[j] + tab[k] + tab[l] == 0);
                }
            }
        }
    }
    return sum;
}